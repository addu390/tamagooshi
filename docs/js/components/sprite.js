export function spriteSvg(art) {
  const rows = art.length, cols = art[0].length;
  let cells = "";

  for (let r = 0; r < rows; r++) for (let c = 0; c < cols; c++) if (art[r][c] === "#") {
    cells += `<rect x="${c}" y="${r}" width="1.06" height="1.06"/>`;
  }

  return `<svg viewBox="0 0 ${cols} ${rows}" fill="currentColor" aria-hidden="true">${cells}</svg>`;
}
