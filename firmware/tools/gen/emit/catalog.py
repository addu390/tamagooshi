import importlib.util
import json
import os

from gen.features.apps import APPS
from gen.features.games import GAMES
from gen.features.mascots import MASCOT_CATEGORIES, MASCOTS
from gen.images import logo_mask, pack_mask
from gen.network.transports import LINKS, PROTOCOLS
from gen.platform.boards import flash_catalog
from gen.ui.themes import THEMES
from gen.ui.typefaces import TYPEFACES

LINK_DESC = {"ble": "Bluetooth LE, always on", "wifi": "Wi-Fi"}
PROTOCOL_DESC = {"gatt": "GATT (native)", "mqtt": "MQTT"}


def _repo():
    here = os.path.dirname(os.path.abspath(__file__))
    return os.path.abspath(os.path.join(here, "..", "..", "..", ".."))


def _agents():
    path = os.path.join(_repo(), "hub", "src", "features", "buddy", "agents", "catalog.py")
    spec = importlib.util.spec_from_file_location("tama_agent_catalog", path)
    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    return [[aid, label] for aid, label in module.AGENTS.items()]


def _default_logo(brand="gooshi"):
    cfg = os.path.join(_repo(), "brands", brand, "config.yaml")
    if not os.path.exists(cfg):
        return None
    try:
        import yaml
    except ImportError:
        return None
    with open(cfg, "r", encoding="utf-8") as fh:
        data = yaml.safe_load(fh) or {}
    src = (data.get("brand") or {}).get("logo")
    if not src:
        return None
    w, h, rows = logo_mask(src, os.path.dirname(cfg))
    return {"w": w, "h": h, "bits": pack_mask(w, h, rows), "name": src}


def catalog():
    return {
        "boards": flash_catalog(),
        "themes": list(THEMES),
        "typefaces": [[tid, spec.get("label", tid)] for tid, spec in TYPEFACES.items()],
        "games": [[gid, meta.get("desc", gid)] for gid, meta in GAMES.items() if not meta.get("soon")],
        "apps": [[aid, meta.get("desc", aid)] for aid, meta in APPS.items()],
        "agents": _agents(),
        "packs": {name: [[mid, MASCOTS[mid]["label"]] for mid in ids]
                  for name, ids in MASCOT_CATEGORIES.items()},
        "transports": {
            "links": [[link, LINK_DESC.get(link, link)] for link in LINKS],
            "protocols": {link: [[p, PROTOCOL_DESC.get(p, p)] for p in protos]
                          for link, protos in PROTOCOLS.items()},
        },
        "logo": _default_logo(),
    }


def render(data):
    return ("// Generated by firmware/tools/gen -- do not edit.\n"
            "window.TAMA_CATALOG = " + json.dumps(data, ensure_ascii=False, indent=2) + ";\n")


def render_module(data):
    return ("// Generated by firmware/tools/gen -- do not edit.\n"
            "export default " + json.dumps(data, ensure_ascii=False, indent=2) + ";\n")
