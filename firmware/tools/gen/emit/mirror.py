import os

WIRE_SRC = os.path.join("hub", "console", "js", "wire")
WIRE_DST = os.path.join("website", "docs", "js", "gen", "wire")
HEADER = "// Generated by firmware/tools/gen from hub/console/js/wire/{name} -- do not edit.\n"


def mirrored(repo):
    src_dir = os.path.join(repo, WIRE_SRC)
    for name in sorted(os.listdir(src_dir)):
        if not name.endswith(".js"):
            continue
        with open(os.path.join(src_dir, name), "r", encoding="utf-8") as fh:
            yield name, HEADER.format(name=name) + fh.read()


def sync(repo):
    dst_dir = os.path.join(repo, WIRE_DST)
    os.makedirs(dst_dir, exist_ok=True)

    written = []
    for name, content in mirrored(repo):
        path = os.path.join(dst_dir, name)
        with open(path, "w", encoding="utf-8") as fh:
            fh.write(content)
        written.append(path)
    return written


def stale(repo):
    out = []
    for name, content in mirrored(repo):
        path = os.path.join(repo, WIRE_DST, name)
        if not os.path.exists(path):
            out.append(name)
            continue
        with open(path, "r", encoding="utf-8") as fh:
            if fh.read() != content:
                out.append(name)
    return out
